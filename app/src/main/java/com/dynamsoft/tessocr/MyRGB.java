package com.dynamsoft.tessocr;

public class MyRGB {
	
	private char R;
	private char G;
	private char B;
	
	public char getR() {
		return R;
	}
	public void setR(char r) {
		R = r;
	}
	public char getG() {
		return G;
	}
	public void setG(char g) {
		G = g;
	}
	public char getB() {
		return B;
	}
	public void setB(char b) {
		B = b;
	}
	
	

}
